package com.xnote.manage.modules.user.bean;

/**
 * @DESC:
 * @ClassName: User
 */
public class User {
    private long id;
    private int age;
    private String name;

    public User() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
